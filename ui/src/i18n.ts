import { ref } from 'vue'

interface MessageTree {
  [key: string]: string | MessageTree
}

export type Locale = 'zh-CN' | 'en-US' | 'es-ES' | 'fr-FR' | 'ja-JP' | 'de-DE'

export const localeOptions: Array<{ value: Locale; label: string }> = [
  { value: 'zh-CN', label: '中文' },
  { value: 'en-US', label: 'English' },
  { value: 'es-ES', label: 'Español' },
  { value: 'fr-FR', label: 'Français' },
  { value: 'ja-JP', label: '日本語' },
  { value: 'de-DE', label: 'Deutsch' }
]

const DEFAULT_LOCALE: Locale = 'zh-CN'
const LOCALE_STORAGE_KEY = 'simple-abtest.locale'
const isLocale = (value: unknown): value is Locale => localeOptions.some(option => option.value === value)

const loadStoredLocale = (): Locale => {
  if (typeof window === 'undefined') return DEFAULT_LOCALE
  try {
    const stored = window.localStorage.getItem(LOCALE_STORAGE_KEY)
    return isLocale(stored) ? stored : DEFAULT_LOCALE
  } catch {
    return DEFAULT_LOCALE
  }
}

const persistLocale = (nextLocale: Locale) => {
  if (typeof window === 'undefined') return
  try {
    window.localStorage.setItem(LOCALE_STORAGE_KEY, nextLocale)
  } catch {
    // ignore storage failures and keep runtime locale working
  }
}

